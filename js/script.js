/*      MODAL COMPLETO      */
const leticiaOpenModal = document.querySelector('#open-modal');
const leticiacloseModal = document.querySelector('#close');
const leticiaModal = document.querySelector('#modal');
const leticiafade = document.querySelector('#fade');

const LeticiatoggleModal = () => {
    leticiaModal.classList.toggle('hide');
    leticiafade.classList.toggle('hide');
}

[leticiaOpenModal, leticiacloseModal, leticiafade].forEach((elemento) => {
    elemento.addEventListener('click', () => LeticiatoggleModal());
    
});



